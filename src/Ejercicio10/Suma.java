package Ejercicio10;

public class Suma {
   public int sumar(int suma1,int suma2){
        return suma1+suma2;
    }
    public int sumar(int suma1,int suma2,int suma3){

        return suma1+suma2+suma3;
    }
    public double sumar (double suma1,double suma2){
        return suma1+suma2;
    }
    public double sumar (double suma1,double suma2,double suma3){
        return suma1+suma2+suma3;
    }

}
